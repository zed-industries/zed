fn build_translations_map() -> HashMap<&'static str, HashMap<&'static str, &'static str>> {
    HashMap::from([
        ("en", translations_en()),
        ("zh-CN", translations_zh_cn()),
        ("ja", translations_ja()),
        ("ko", translations_ko()),
        ("fr", translations_fr()),
        ("de", translations_de()),
        ("es", translations_es()),
        ("pt-BR", translations_pt_br()),
        ("ru", translations_ru()),
    ])
}

static TRANSLATIONS_MAP: RwLock<
    Option<HashMap<&'static str, HashMap<&'static str, &'static str>>>,
> = RwLock::new(None);

fn get_translations_map() -> HashMap<&'static str, HashMap<&'static str, &'static str>> {
    let guard = TRANSLATIONS_MAP.read().unwrap();
    if let Some(map) = guard.as_ref() {
        return map.clone();
    }
    drop(guard);

    let mut write_guard = TRANSLATIONS_MAP.write().unwrap();
    if write_guard.is_none() {
        *write_guard = Some(build_translations_map());
    }
    write_guard.as_ref().unwrap().clone()
}

pub fn get_locale() -> String {
    CURRENT_LOCALE.read().unwrap().to_string()
}

pub fn set_locale(locale: &str) {
    let supported = ["en", "zh-CN", "ja", "ko", "fr", "de", "es", "pt-BR", "ru"];
    if supported.contains(&locale) {
        *CURRENT_LOCALE.write().unwrap() = locale;
    }
}

pub fn t(key: &str) -> String {
    let locale = get_locale();
    let map = get_translations_map();
    map.get(locale.as_str())
        .and_then(|m| m.get(key))
        .copied()
        .unwrap_or_else(|| {
            map.get("en")
                .and_then(|m| m.get(key))
                .copied()
                .unwrap_or(key)
        })
        .to_string()
}

pub fn available_locales() -> Vec<&'static str> {
    vec!["en", "zh-CN", "ja", "ko", "fr", "de", "es", "pt-BR", "ru"]
}

pub fn locale_display_name(locale: &str) -> &'static str {
    match locale {
        "en" => "English",
        "zh-CN" => "简体中文",
        "ja" => "日本語",
        "ko" => "한국어",
        "fr" => "Français",
        "de" => "Deutsch",
        "es" => "Español",
        "pt-BR" => "Português (Brasil)",
        "ru" => "Русский",
        _ => "English",
    }
}

pub fn detect_system_locale() -> &'static str {
    let lang = std::env::var("LANG")
        .or_else(|_| std::env::var("LC_ALL"))
        .or_else(|_| std::env::var("LC_MESSAGES"))
        .unwrap_or_default();

    let lang_lower = lang.to_lowercase();

    if lang_lower.contains("zh") {
        "zh-CN"
    } else if lang_lower.contains("ja") {
        "ja"
    } else if lang_lower.contains("ko") {
        "ko"
    } else if lang_lower.contains("fr") {
        "fr"
    } else if lang_lower.contains("de") {
        "de"
    } else if lang_lower.contains("es") {
        "es"
    } else if lang_lower.contains("pt") {
        "pt-BR"
    } else if lang_lower.contains("ru") {
        "ru"
    } else {
        "en"
    }
}

pub fn init_with_system_locale() {
    let system_locale = detect_system_locale();
    set_locale(system_locale);
}

pub fn init_with_locale(locale: &str) {
    let supported = ["en", "zh-CN", "ja", "ko", "fr", "de", "es", "pt-BR", "ru"];
    if supported.contains(&locale) {
        set_locale(locale);
    } else {
        init_with_system_locale();
    }
}
